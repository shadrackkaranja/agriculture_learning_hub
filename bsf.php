                                <div class="tab-pane fade in active" id="tab16">
                                    <div class="row">
                                        <div class="col-md-12">
                                            <p>The substrates used for hatching the BSF neonates are wheat bran, maize bran and they should be fermented for at least four days. Yeast and molasses are added to these substrates before fermentation begins. The fermented using the molasses and yeast helps in breaking down further the nutrients making them available to the BSF neonate larvae. The molasses also proves the energy to the neonates BSF. The hatched neonates remain in these substrates for maximum of one week then separated for growing feeding. The substrates must be wet throughout to reduce the humidity related motilities of the eggs</p>
                                            <p>
                                                The eggs are not put directly on the substrate to reduce the eggs mortality thus low hatchability due to soaking of the eggs. So, to control these eggs are put on a goose wire above the crates and coved with a white shade net to prevent houseflies from laying eggs into the substrate. Splitting is done according the BSF neonates weight and concentration, which ranges between 200g to 250g per larvae production crates
                                            </p>
                                        </div>
                                    </div>
                                </div>
                                <div class="tab-pane fade" id="tab17">
                                    <div class="row">
                                        <div class="col-md-12">
                                            <p>Under good quality substrate the larvae can be harvested after two weeks, but can be extended to three weeks if the substrate/ wastes are not good quality. To facilitate easy harvesting, the 2nd phase fed substrate should be allowed time dry up. The harvesting is done using a coffee wire, where by degraded substrates larvae mixture is poured in small quantities on to the sieve and shaken for two second to prevent larvae penetration through the sieve. During harvesting, the number of crates harvested and larvae weight is recorded</p>
                                            <p>The most economical method of killing the larvae is through hot water, where the larvae is submerged into a hot boiling water with a sack for about 15 seconds. The hot water will not only kill the larvae but also kill all disease-causing microbes. But they can also be subjected under very cold temperature in a freezer to kill them, but this method is very expensive thus a common farmer cannot afford. Some farmers are also roasting them but this method is not efficient as it is time consuming.
                                                The simplest method of conservation of the harvested BSF is through the use of sunlight energy. The killed larvae are spread on the porous material to permit the water seepage that facilitates the drying. An oven or green house can also be used to dry the larvae but the initial capital is very expensive. The drying should take a minimum of three days but this can vary depending on the prevailing whether conditions. For open air drying the birds should be kept astray because they are more attracted by the larvae due to its palatability.
                                            </p>
                                            <p><b>Note: <i>A farmer should only proceed to killing and drying when the larvae is to be used in feed formulation, since they can also be used alive e.g. for feeding fish, poultry and pigs</i></b></p>
