                                <h2 class="text-uppercase text-theme-colored font line-bottom line-height-1 mt-0 mb-0">Black Soldier<span class="text-theme-color-2 font-weight-400"> Fly</span></h2>
                                <h4 class="text-theme-colored">Black soldier fly (BSF) is a newly domesticated insect which falls under the Stratiomyidae family and dipteran order, known as the 'true flies' or "two-winged flies"</h4>
                                <p>They exist in the wild and are commonly found in habitats suitable for larval development such as damp places with animal waste, rotten fruit or any decaying organic matter. The fly is not regarded as a pest species, adult fly typically lives up to nine days without the need to feed as it relies on fat body reserve that was acquired during its larval developmental stages with free access of water, thus keeps off homesteads.</p>
                                <p>Its developmental stages incudes; Eggs, larvae, prepupa, pupa and the fly. A life cycle that takes about 45 days; eggs four (4) days to hatch, eighteen (18) days as larval stage. Transformation from prepupa into pupa fourteen (14) days while adults flies only live for a maximum of nine (9) days</p>
                                <p>The insect requires an optimal rearing condition of approximately 27-34°C and 60-70% temperature and relative humidity respectively.
                                    The rearing of the insects is an emerging innovation widely spreading world over due to its several economic, social and environmental roles in human lives. The insect is a source of good protein that can be used in animal feeds, they have the ability to biodegrade the large quantities of organic wastes thus reduce the pollution and human health risk from huge unhygienic wastes, it gives a good bio fertilizer from the biodegraded wastes or substrates, direct and indirect employment creation and incomes source
                                </p>
