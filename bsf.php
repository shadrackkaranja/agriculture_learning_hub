                                            <p>This done in three phases. The first phase of feeding ranges between 8 to 9 days with a total of 10 kg of substrate, while second is seven (7) days with 4 kg of substrate. After every feeding phase, the larvae is sieved twice using coffee and goose wire to separate the larvae and the frass. Feeding of larvae for feed production only last for 9 days which is first phase, while for pupa the feeding goes through up to the third phase. During this phase, the substrate should be turned regularly to ensure good aeration and temperature moderations within the crates</p>
