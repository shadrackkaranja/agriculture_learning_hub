                                <hr>
                                <p class="text-gray">To achieve optimum productivity in the love cage, the following activities should be carried out:</p>
                                <ul class="list theme-colored">
                                    <li>More room/space must be provided for flies to fly freely from one corner to the next by having few crates of a maximum eight pupa holding creates (60cm by 40cm by 12 cm) in 4 by 4 by 4 ft. love cage</li>
                                    <li>Shading half of the love cage on top especially with a carton to provide a dark area for eggs deposition and also to enables the flies to shift controlling their temperature i.e. they will move from covered to uncovered area when temperatures are low</li>
                                    <li>Provision of sugar crystals and sprinkling of sugar syrup on the moist cloth to boost their energy for reproductive activity and prolonged lifespan of the BSF therefore more eggs are collected </li>
                                    <li>Routine removal of dead flies everyday especially very early in the morning and late in the evening, this eliminate bad smell from such dead flies and to ensure the live flies only lay eggs on the attractant but not on the dead flies</li>
                                    <li>Routine washing and disinfecting of the love cage nets to reduce the disease-causing pathogens and maintaining the hygiene and cleanliness </li>
                                    <li>Love cage humidity maintance to ensure it is maintained, moist blankets are spread on the love cage floor and also containers containing water is included in the love cage with a face towels dipped in them. The love cages are sprinkled with water to moist the love cage net but only when the temperature is relatively high.</li>
                                    <li>Love cage labeling and pining of data entry form to tract the activities including, date of pupa introduction, eggs collection, emptying the cage and washing.</li>
                                    <li>Eggs collection, eggs are collected every three days and everything is removed including the attractant into the BSF neonates hatching crates then cleaning the attractant holding dish.</li>
                                    <li>Emptying the pupa crates from the love cage, here the maximum days for the pupa holding crates spends in the love cage is 14 days, then everything is removed and net is cleaned</li>
                                </ul>
