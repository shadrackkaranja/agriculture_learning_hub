                                            <p>Note:<i style="color: orange;"> The larvae number to be put in a crate should be estimated by scooping a small quantity mixture of larvae and substrate of about 100g then count the number of larvae within that quantity mixture. This is very important especially during the feeding of larvae and in determining the weight of the substrate larvae mixture. Since 0.6 to 0.8g per larvae for feed utilization and 1.8g per larvae for pupa production. Each crate measuring 60 by 40 by 12 cm should hold up between 6000-7000 larvae</i></p>
