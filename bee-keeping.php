                                <h2 class="text-uppercase mt-0 font-28 line-bottom">Honey <span class="text-theme-color-2 font-weight-400">Processing</span></h2>
                                <h4 class="text-theme-colored">Processing of honey is a chronological procedure that involves heating, filtration and moisture reduction</p>
                                    <div id="accordion4" class="panel-group accordion">
                                        <div class="panel">
                                            <div class="panel-title"> <a data-parent="#accordion4" data-toggle="collapse" href="#accordion11s" class="active" aria-expanded="true"> <span class="open-sub"></span>Honey Bulking </a> </div>
                                            <div id="accordion11s" class="panel-collapse collapse in" role="tablist" aria-expanded="true">
                                                <div class="panel-content" style="color: #666 !important;">
                                                    <p>The objective is to ensure that incoming raw honey is of high quality and is stored properly. The honey received is tested for pollen, maturity, moisture, smoke contamination, quantity and other debris that might be in honey.</p>
                                                    <p>Therefore, Raw honey should</p>
                                                    <ul>
                                                        <li>• Be stored away from inappropriate or potentially harmful chemicals</li>
                                                        <li>• Be stored in an area that is clean, tidy and free from pests</li>
                                                        <li>• Be stored at the appropriate temperature as per manufacturer’s instructions. Be stored at the floor in closed containers or pack when not processed</li>
                                                        <li>• Be stored with clear identification</li>
                                                        <li>• Be moved to appropriate storage rooms as soon as possible after delivery</li>
                                                        <li>• Be Stored on racks, shelves or pallets to ensure no contact with the floor</li>
                                                        <li>• Be kept separate from maintenance compounds and other hazardous materials and properly labelled or identified.</li>
                                                    </ul>
                                                </div>
                                            </div>
                                        </div>
                                        <div class="panel">
                                            <div class="panel-title"> <a class="collapsed" data-parent="#accordion4" data-toggle="collapse" href="#accordion12s" aria-expanded="false"> <span class="open-sub"></span>Honey Extraction</a> </div>
                                            <div id="accordion12s" class="panel-collapse collapse" role="tablist" aria-expanded="false" style="height: 0px;">
                                                <div class="panel-content" style="color: #666 !important;height: 451px;overflow-x: hidden;overflow-y: auto;">
                                                    <h3 style="font-size: 20px;">Deboxing and uncapping</h3>
                                                    <ul>
                                                        <li>• Deboxing must be done in a manner that will minimize transfer of contamination from boxes to combs or from one container to the other</li>
                                                        <li>• Uncapping equipment (i.e knives, blades, prickers, forks) must be used in a manner (i.e in a hygienic and good working condition) that minimizes contamination</li>
                                                    </ul>
                                                    <h3 style="font-size: 18px;">Extraction</h3>
                                                    <ul>
                                                        <li>• The extractor must be clean and dry before the start of extraction</li>
                                                        <li>• Do not allow the build-up of wax, caramelised honey and foreign matter (e.g.wax, dirt, dead bees) the extractor.</li>
                                                        <li>• Cover the extractor with a lid when not in use (e.g. overnight) to prevent the entry of pests and to prevent steam and water from contaminating honey.</li>
                                                        <li>• Honey that has been spilt onto the processing floor must not be used for human consumption. Provided it is not contaminated with any chemical substance, spilt honey may be used for animal consumption.</li>
                                                        <li>• Any contaminated honey must be clearly identified as not intended for human consumption.</li>
                                                    </ul>
                                                    <h3 style="font-size: 18px;">Transferring honey through the sump tank</h3>
                                                    <ul>
                                                        <li>• Remove wax and other debris from the sump tank at least daily</li>
                                                        <li>• Construct and locate sump tanks in such a manner that prevents contamination of the honey from water (including splashes from the floor), condensates, dust and other contaminants.</li>
                                                        <li>• If honey that is separated from cappings is collected, it must be added to the sump or honey tank in a hygienic manner </li>
                                                    </ul>
                                                    <h3 style="font-size: 18px;">Straining/filtering</h3>
                                                    <ul>
                                                        <li>• Strainers and filter must be made of materials that is suitable for food</li>
                                                        <li>• Ensure the mesh size of the strainer or filter is suitable for the type of material that is being filtered from honey.</li>
                                                        <li>• Strainers or filters must be maintained in good condition and must not be a source of contamination.</li>
                                                    </ul>
                                                    <h3 style="font-size: 18px;">Holding of extracted honey in tanks</h3>
                                                    <ul>
                                                        <li>• Holding containers/tanks must be clean and dry before being filled with honey to minimize contamination.</li>
                                                        <li>• Tanks containing honey must be protected from the entry of bees and other insects, condensates, dust and other contaminants </li>
                                                    </ul>
                                                    <h3 style="font-size: 18px;">Filling and storage into food grade containers/drums</h3>
                                                    <ul>
                                                        <li>• Honey must be filled into bulk containers in a manner that prevents contamination of honey.</li>
                                                        <li>• Bulk containers must be sealed with tightly fitted bungs or lids to prevent contamination.</li>
                                                        <li>• Permanently mark or identify full bulk containers so that it can be easily linked to the relevant harvest declaration.</li>
                                                    </ul>
                                                </div>
                                            </div>
                                        </div>
                                        <div class="panel">
                                            <div class="panel-title"> <a class="collapsed" data-parent="#accordion4" data-toggle="collapse" href="#accordion13s" aria-expanded="false"> <span class="open-sub"></span>Processing of different products</a> </div>
                                            <div id="accordion13s" class="panel-collapse collapse" role="tablist" aria-expanded="false" style="height: 0px;">
                                                <div class="panel-content" style="color: #666 !important;height: 651px;overflow-x: hidden;overflow-y: auto;">
                                                    <h3 style="font-size: 18px;">Procedure for the processing of honey</h3>
                                                    <ul>
                                                        <li>1. Receiving and holding of supers</li>
                                                        <li>2. When the loading bay is located inside the building, measures must be taken to prevent contamination of materials, products, and the processing environment from dust, dirt, bees and other insects, fumes, and other environmental contaminants during the entry and exit of vehicles, and during unloading of supers or honey buckets</li>
                                                        <li>3. Supers or honey buckets must be transported on clean trucks and covered during transportation in a manner that minimizes dust, engine fumes and other road-based contamination.</li>
                                                        <li>4. Honey supers/containers that are infested (e.g with wax moth) excessively dirty, or contaminated with faecal materials (e.g. bovine, rodent or bird faeces) must not be accepted for processing until the contamination is removed.</li>
                                                        <li>5. Full honey supers must be stored in a suitable storage area.</li>
                                                        <li>6. Supers or honey buckets that are not stored in a room or will not be processed immediately must be protected from moisture and contamination from dust, bees and other insects, fumes and other environmental contaminants</li>
                                                        <li>7. Minimize the entry of live bees into the storage are, hot room or extraction room</li>
                                                    </ul>
                                                    <h3 style="font-size: 18px;">Processing of comb honey</h3>
                                                    <ul>
                                                        <li>1. The external surface of stored honey drums must be clean (e.g washed in an appropriate manner) to minimize contamination of honey as it is removed from the buckets/drums</li>
                                                        <li>2. Honey must be transferred into buckets/drums in a hygienic manner</li>
                                                        <li>3. Creaming containers must be protected from the entry of bees and other insects, condensates, dust and other contaminants</li>
                                                        <li>4. Creaming tanks, including the mixer blade mechanism, must be in good mechanical condition and must not be a source of contamination (e.g. metal fragments, lubricants)</li>
                                                        <li>5. Starter honey must not be a source of contamination and must be mixed into the product in a hygienic manner</li>
                                                    </ul>
                                                    <h3 style="font-size: 18px;">Processing of comb honey</h3>
                                                    <ul>
                                                        <li>1. Combs that are infested, travel stained, dirty, contain brood or fermented honey must not be processed into comb honey</li>
                                                        <li>2. Inspect comb honey using a light source or similar device to detect any remaining wire or other foreign matter</li>
                                                        <li>3. Honey combs must be adequately packed or protected during freezing to ensure that contamination from other sources is prevented</li>
                                                    </ul>
                                                    <h3 style="font-size: 18px;">Processing of pollen</h3>
                                                    <h4 style="font-size: 16px;">Receipt of pollen</h4>
                                                    <ul>
                                                        <li>1. Pollen must also be free from the presence of dead bees, wax, insect parts, wood, dust and other foreign matter</li>
                                                        <li>2. The operator must ensure that pollen received for processing is fit for intended purpose i.e. free from mould (it is not fit for human consumption and must be discarded) and contamination from rodents droppings and pests such as cockroaches and ants</li>
                                                    </ul>
                                                    <h4 style="font-size: 16px;">Freezing of pollen</h4>
                                                    <ul>
                                                        <li>1. Place fresh pollen in a freezer without unnecessary delay especially if the pollen is wet</li>
                                                        <li>2. Contamination of the pollen must be prevented during freezing. Pollen must be properly packed and identified</li>
                                                        <li>3. The freezer must have the capability to quickly freeze pollen to the required temperature</li>
                                                        <li>4. The pollen must be loaded in the freezer in such a manner that allows effective freezing of the product</li>
                                                    </ul>
                                                    <h4 style="font-size: 16px;">Drying of pollen</h4>
                                                    <ul>
                                                        <li>1. Drying of pollen must be done in a manner that minimizes the contamination of the product and the growth of any microorganism present in the product.</li>
                                                        <li>2. Dry pollen to a final moisture content sufficient for the preservation of the product considering its intended packaging and storage conditions.</li>
                                                    </ul>
                                                    <h4 style="font-size: 16px;">Cleaning of pollen</h4>
                                                    <ul>
                                                        <li> Dried pollen must be cleaned to ensure that the product is free of all foreign matter such as dead bees, wax, insect parts, wood, dust, and other contaminants.</li>
                                                    </ul>
                                                    <h4 style="font-size: 16px;">Storage of pollen</h4>
                                                    <ul>
                                                        <li>Storage pollen intended for human consumption in a deep freeze or as dried pellets in air tight containers at room temperature</li>
                                                    </ul>
                                                    <h4 style="font-size: 16px;">Labelling of pollen</h4>
                                                    <ul>
                                                        <li>The label on a package of pollen must meet the general labelling requirements under food standard codes</li>
                                                    </ul>
                                                    <h3 style="font-size: 18px;">Processing of propolis</h3>
                                                    <ul>
                                                        <li>1. Raw propolis collected from the hives directly or by use of propolis mats can be stored until processing time</li>
                                                        <li>2. Processing often consist of freezing the propolis mats and scraping off the propolis</li>
                                                        <li>3. The propolis is stored and/or packaged</li>
                                                        <li>4. Whilst there are no regulatory requirements for label warnings or advisory statements during processing is still advisable to identify and manage propolis as an allergen during the processing steps</li>
                                                        <li>5. Propolis can be sold as a dietary supplement depending on how it is sold</li>
                                                    </ul>
                                                    <h3 style="font-size: 18px;">Processing of beeswax</h3>
                                                    <ul>
                                                        <li>1. Beeswax is commonly used by beekeepers to make foundation sheets, some in cosmetics and other uses</li>
                                                        <li>2. Beeswax can be processed by mixing left over from honey processing with water, boiling, sieving into a container and then allow to cool down</li>
                                                        <li>3. Cappings from framed honey combs can also be rendered into beeswax</li>
                                                        <li>4. After processing, wax needs to be cooled and stored in dry condition and safe from dust</li>
                                                    </ul>
                                                    <h3 style="font-size: 18px;">Bee venom collection and processing</h3>
                                                    <ul>
                                                        <li>1. Bee venom collection includes shocking the bees to sting a surface e.g. glass, and the venom is then collected by scraping</li>
                                                        <li>2. Bee venom is stored refrigerated and kept in dark containers e.g. glass bottles</li>
                                                        <li>3. Where glass is used, a breakage procedure should be developed</li>
                                                    </ul>
                                                </div>
