                                    <h3 class="text-theme-colored">Apiary</h3>
                                    <p>An apiary is a location where beehives of honey bees are kept. Apiaries come in many sizes and can be rural or urban depending on the honey production operation. Furthermore, an apiary may refer to a hobbyist's hives or those used for commercial or educational usage.</p>
