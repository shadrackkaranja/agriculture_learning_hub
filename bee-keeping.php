                                                <h4 class="line-bottom mb-10">Candles</h4>
                                                <p>The basic elements of candle are the solid bees wax as fuel for the flame and a wick, which serves to bring the molten wax to flame. The best material for the wick is fibre which burns with very little ash at low temperatures. Pure cotton is the best. Several thin cotton threads should be braided or plaited together until the desired thickness is reached. Twisting of the thread is not recommended, since they might unwind during burning and then create an irregular flame consuming much fuel. Commercially produced candles wick can be purchased in speciality shops.</p>
                                                <p>The wick needs to be in the centre of the candle for even burning. The diameter of the wick in proportion to candle diameter is important to maximize the light obtained from the quantity of wax and to prevent wax dripping down the side of the candle. Thicker candles need thicker wicks, but thick candles with a relatively thin wick burn longer and give less light, since the flame is shaded by the remaining edges of the candle. The precise ratio depends on the purpose of the candle and should be determined by experiment</p>
                                                <p>Beeswax for candle needs to be extremely clean and free from all impurities (propolis or pollen) otherwise the candle will sputter while burning, give irregular light and possibly be splattering hot beeswax. Beeswax purchased from most beekeepers must usually be reprocessed at least once more in clean water</p>
