                                                        <li>2. Safe from strong direct sunshine, windy areas and theft</li>
                                                        <li>3. Accessible to water and bee plants nearby</li>
                                                        <li>4. Away from swampy areas, rocky and smelly places. This is to say that topography should be considered to avoid any inconveniences while working the hives</li>
