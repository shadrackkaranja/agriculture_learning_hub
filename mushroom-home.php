                <div class="owl-carousel-4col" data-dots="true">
                  <div class="item">
                    <div class="service-block bg-white" style="height: 401px;">
                      <div class="content text-left flip p-25 pt-12" style="border: none;">
                        <h4 class="line-bottom mb-10"><b>(A) Substrate Preparation</b></h4>
                        <p>Select the right substrate material and shred in to small sizes. The shredding could be done manually or by use of machine. 1kg of dry substrate when wet is approximately 3.14kg.
                          2kg spawn is supposed to plant 15kg (dry matter)
                        </p>

                      </div>
                    </div>
                  </div>
                  <div class="item">
                    <div class="service-block mb-md-30 bg-white" style="height: 401px;">
                      <div class="content text-left flip p-25 pt-12" style="border: none;">
                        <h4 class="line-bottom mb-10"><b>(B) Substrate Sterilization</b></h4>
                        <p>The sterilization is meant to kill all the microorganism that might interfere with the growth of mushroom. This can be done using two methods;</p>
                        <ul class="list theme-colored">
                          <li>Steaming </li>
                          <li>Immersion </li>
                        </ul>
                      </div>
                    </div>
                  </div>
                  <div class="item">
                    <div class="service-block mb-md-30 bg-white" style="height: 401px;">
                      <div class="content text-left flip p-25 pt-12" style="border: none;">
                        <h4 class="line-bottom mb-10"><b>(C) Spawning</b></h4>
                        <p>This refers to mixing of the substrate and spawn. Spread the substrate on a clean sterilized polythene sheet and use Squeeze/fist method to verify 70% moisture content which is verified by 2-3 drops of water after squeezing. Mix the spawn thoroughly with the spawn and once ready, you pack in the growing gardens</p>
                      </div>
                    </div>
                  </div>
                  <div class="item">
                    <div class="service-block mb-md-30 bg-white" style="height: 401px;">
                      <div class="content text-left flip p-25 pt-12" style="border: none;">
                        <h4 class="line-bottom mb-10"><b>(D) Dark Room</b></h4>
                        <p>Once the gardens have been well packed, they are stored in the dark room for 14 to 21 days for colonization. During this incubation period, ensure temperatures are not beyond 28 degrees Celsius.</p>
                      </div>
                    </div>
                  </div>
                  <div class="item">
                    <div class="service-block mb-md-30 bg-white">
                      <div class="content text-left flip p-25 pt-12" style="border: none;">
                        <h4 class="line-bottom mb-10"><b>(E) Production Room</b></h4>
                        <p>After full colonization of the substrate, transfer the planted gardens to the growing room.
                          Make sure humidity is maintained, which should not be less than 90%.use a spray pump to humidify the room.
                          After 6-7 days, pin heads will start to appear, make holes on the gardens for the mushrooms to sprout out and in 2-4 days they will be ready for harvesting.
                        </p>
                      </div>
                    </div>
                  </div>
                  <div class="item">
                    <div class="service-block mb-md-30 bg-white" style="height: 401px;">
                      <div class="content text-left flip p-25 pt-12" style="border: none;">
                        <h4 class="line-bottom mb-10"><b>(F) Harvesting</b></h4>
                        <p>Mushrooms are harvested over a period of 2 to 4 days in a 7 to 10 day cropping cycle (also known as a flush). Once the mature mushrooms are harvested, the next flush moves towards maturity.
                        </p>
                      </div>
                    </div>
                  </div>
                  <div class="item">
                    <div class="service-block mb-md-30 bg-white" style="height: 401px;">
