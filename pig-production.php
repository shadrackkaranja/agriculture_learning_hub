                <div class="owl-carousel-4col" data-dots="true">
                  <div class="item">
                    <div class="service-block bg-white" style="height: 741px;">
                      <div class="content text-left flip p-25 pt-12" style="border: none;">
                        <h4 class="line-bottom mb-10">(A) Management of piglets</h4>
                        <p>Feeding-; Piglets should be fed with colostrum within the 1st 72 hours after farrowing.
                          For every piglet a sow has the feed is increase by 0.25kgs.
                        </p>
                        <p>Introduce creep feeds higher in protein content up to 22-24% CP 9 -10 days after birth. It assists in stimulation of the gastro-intestinal tract ability when weaning
                        </p>
                        <p>
                          Heating-; Put the piglets under a heat source
                          Teeth clipping-; Teeth clip 4 pairs of needle teeth within 24 hours to avoid injuries to the udder and to other piglets
                        </p>
                        <p>
                          Iron injection-; Inject the piglets with 2mls of injectable iron within the 1st 3 days of birth to prevent the piglets from suffering from anemia.
                          Castration-; Carried on male piglets 21 days after birth unless the males are for breeding. Castration helps to control in-breeding and removal of boar taint on meat.
                        </p>

                      </div>
                    </div>
                  </div>
                  <div class="item">
                    <div class="service-block mb-md-30 bg-white" style="height: 741px;">
                      <div class="content text-left flip p-25 pt-12" style="border: none;">
                        <h4 class="line-bottom mb-10">(B) Management of weaners</h4>
                        <p>Reduce the sow feeds by a ¼ kgs per feeding one week before weaning to reduce the milk production, deworm the piglets to control round worms, weigh the piglets and group them according to their weight and size. Newly weaned piglets weigh between 13-16 kgs. Always remove the sow from the pen leaving the piglets to prevent them from being stressed in new environment. Feed the weaned piglets with ½ kg of sow and weaner meal per day </p>

                      </div>
                    </div>
                  </div>
                  <div class="item">
                    <div class="service-block mb-md-30 bg-white" style="height: 741px;">
                      <div class="content text-left flip p-25 pt-12" style="border: none;">
                        <h4 class="line-bottom mb-10">(C) Management of growing pigs (20-50) kgs</h4>
                        <p>Pigs weight gain is between 400-800gms per day.
                          Feed the pig for 8 weeks or 2 months with sow and weaner meals as follows;
                          2 months=0.5kgs,3 months=1kg,4months=1.25kg per pig per day
                        </p>
                      </div>
                    </div>
                  </div>
                  <div class="item">
                    <div class="service-block mb-md-30 bg-white" style="height: 741px;">
                      <div class="content text-left flip p-25 pt-12" style="border: none;">
                        <h4 class="line-bottom mb-10">(D) Management of fattener pigs (50-110) kgs</h4>
                        <p>The pig should reach 50 kgs live weight at the end of 4 months</p>
                        <p>Gradually introduce pig finisher meal by mixing ½ sow and weaner meal with ½ pig finisher for one week at a rate of 1¼ kgs per pig per day. Slowly increase the amount until the pig is eating 3kgs per day.</p>
                        <p><b>Do not increase the feed beyond this amount</b></p>
                      </div>
                    </div>
                  </div>
                  <div class="item">
                    <div class="service-block mb-md-30 bg-white" style="height: 741px;">
                      <div class="content text-left flip p-25 pt-12" style="border: none;">
                        <h4 class="line-bottom mb-10">(E) Management of the sow</h4>
                        <p>Sow should be large and healthy to carry more piglets.it should have more than 14 teats farrowing 12-20 piglets per litter and weaning a minimum of 10 piglets
                        </p>
                        <p>
                          A sow should be fed with 4kg of sow and weaner meal per day. An extra feed10-20 % is given to the animal 3 days before mating to increase the conception rate and implantation
                        </p>
                      </div>
                    </div>
                  </div>
                  <div class="item">
                    <div class="service-block mb-md-30 bg-white" style="height: 741px;">
                      <div class="content text-left flip p-25 pt-12" style="border: none;">
                        <h4 class="line-bottom mb-10">(F) Management of the boar</h4>
                        <p>A boar starts serving at the age of 8 months with two services per week for the first 2 months after which it can serve for up to 6 services per week.
                          A boar should be fed with a constant ration of 2.75kgs/day.
                        </p>
                        <p>
                          Each boar Should be kept in its own pen provided with room for exercise and a mating ratio of 1:20. When mating, transfer the sow to the boar
                          Should be dewormed after every 4 months
                        </p>
                      </div>
                    </div>
                  </div>
                </div>
